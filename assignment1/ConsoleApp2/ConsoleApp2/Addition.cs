namespace ConsoleApp2;

public static class Addition
{
    public static int AddNumber(int a, int b)
    {
        return a + b;
    }
    public static double AddNumber(double a, double b)
    {
        return a + b;
    }

    public static int AddNumber(int a, int b, int c)
    {
        return a + b + c;
    }

}